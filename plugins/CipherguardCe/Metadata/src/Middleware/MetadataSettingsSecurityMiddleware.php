namespace Cipherguard\Metadata\Middleware;

use Cake\Core\Configure;
use Cake\Http\Exception\ForbiddenException;
use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;

class MetadataSettingsSecurityMiddleware implements MiddlewareInterface
{
    public const CIPHERGUARD_SECURITY_METADATA_SETTINGS_EDITION_DISABLED =
        'cipherguard.security.metadata.settings.editionDisabled';

    /**
     * @param \Psr\Http\Message\ServerRequestInterface $request The request.
     * @param \Psr\Http\Server\RequestHandlerInterface $handler The handler.
     * @return \Psr\Http\Message\ResponseInterface The response.
     */
    public function process(
        ServerRequestInterface $request,
        RequestHandlerInterface $handler
    ): ResponseInterface {
        if (Configure::read(self::CIPHERGUARD_SECURITY_METADATA_SETTINGS_EDITION_DISABLED)) {
            throw new ForbiddenException(__('Metadata settings endpoints are disabled.'));
        }

        return $handler->handle($request);
    }
}
